require 'set'

input = DATA.read.lines(chomp: true).map(&:chars).map(&:freeze).freeze

start = nil
target = nil

possible_starts = []

elevations = input.map.with_index { |row, y|
  row.map.with_index { |cell, x|
    case cell
    when "S"
      start = [x, y]
      0
    when "E"
      target = [x, y]
      ?z.ord - ?a.ord
    when "a"
      possible_starts << [x, y]
      0
    else
      cell.ord - ?a.ord
    end
  }
}

def shortest_path(grid, start, target)
  height = grid.length
  width = grid.first.length

  q = Set[start]
  dist = Hash.new(Float::INFINITY)
  prev = Hash.new

  dist[start] = 0

  neighbors = ->(coords) {
    x, y = coords
    [[1, 0], [-1, 0], [0, 1], [0, -1]].map { |x_, y_|
        [x + x_, y + y_]
      }.filter { |x_, y_|
        in_bounds = x_ >= 0 && x_ < width && y_ >= 0 && y_ < height

        if in_bounds
          grid[y_][x_] <= grid[y][x] + 1
        else
          false
        end
      }
  }

  while !q.empty?
    u = q.min_by { |v| dist[v] }
    q.delete(u)

    break if u == target

    neighbors[u].each { |v|
      alt = dist[u] + 1
      if alt < dist[v]
        dist[v] = alt
        q << v
        prev[v] = u
      end
    }
  end

  path = []
  u = target

  if !prev[u].nil? || u == start
    while !u.nil?
      path.unshift(u)
      u = prev[u]
    end
  else
    return nil
  end

  [path, dist[target]]
end

p shortest_path(elevations, start, target)[1]

p possible_starts.filter_map { |coord|
  shortest_path(elevations, coord, target)&.[](1)
}.min

