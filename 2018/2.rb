require 'set'

ids = DATA.readlines.map(&:chomp)
two = 0
three = 0
ids.each do |id|
  counts = id.chars.group_by(&:itself).values.map(&:length).uniq
  two += 1 if counts.include? 2
  three += 1 if counts.include? 3
end
checksum = two * three
puts "Part 1: #{checksum}"

pairs = Set.new
ids.each do |id|
  (0...id.length).each do |i|
    pair = [id[0...i], id[i+1..-1]]
    if pairs.include? pair
      puts "Part 2: #{pair.join}"
      exit
    end
    pairs.add(pair)
  end
end

