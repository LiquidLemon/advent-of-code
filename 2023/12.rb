def possible(pattern)
  if pattern.length == 1
    return pattern == "?" ? Set[[], [1]] : Set[[1]]
  end

  groups = possible(pattern[1..])
  results = Set[]

  if pattern[0] == "?"
    results |= groups

    groups.each { |group|
      occupied = group.sum + group.length - 1
      if occupied == pattern.length - 1
        group.length.times { |i|
          copy = group.dup
          copy[i] += 1
          results << copy
        }
      elsif occupied = pattern.length - 2
        results << group + [1]
      end
    }
  else
    groups.each { |group|
      occupied = group.sum + group.length - 1
      if occupied == pattern.length - 1
        copy = group.dup
        copy[0] += 1
        results << copy
      end

      if occupied == pattern.length - 2
        if pattern[1] == "?"
          results << [1] + group
        end

        copy = group.dup
        copy[0] += 1
        results << copy
      elsif group == []
        results << [1]
      end
    }
  end
  #p pattern
  #p groups
  #p results
  #puts

  results
end


def possible_strings(pattern)
  if pattern.empty?
    return [""]
  end

  if pattern[0] == "?"
    possible_strings(pattern[1..]).flat_map { |s| ["#" + s, "." + s] }
  else
    possible_strings(pattern[1..]).map { |s| pattern[0] + s }
  end
end

