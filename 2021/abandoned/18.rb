def explode(number)
  exploded = number.map(&:dup)

  modified = false

  loop do
    index = exploded.find_index { _1[1] == 4 }
    return [exploded, modified] if index.nil?
    left = exploded[index]
    right = exploded[index + 1]

    if index > 0
      exploded[index - 1][0] += left[0]
    end

    if index + 2 < exploded.length
      exploded[index + 2][0] += right[0]
    end

    exploded.delete_at(index)
    exploded[index][0] = 0
    exploded[index][1] -= 1

    modified = true
  end
end

def split(number)
  number = number.map(&:dup)

  modified = false

  loop do
    index = number.find_index { _1[0] >= 10 }

    return [number, modified] if index.nil?

    level = number[index][1] + 1
    left = [(number[index][0].to_f / 2).floor, level]
    right = [(number[index][0].to_f / 2).ceil, level]

    number[index] = left
    number.insert(index + 1, right)

    modified = true
  end
end

def reduce(number)
  loop do
    number, exploded = explode(number)
    number, was_split = split(number)
    break unless exploded || was_split
  end

  number
end

def add(a, b)
  number = a.map { [_1[0], _1[1] + 1] } + b.map { [_1[0], _1[1] + 1] }
  reduce(number)
end

