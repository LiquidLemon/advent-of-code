input = DATA.readlines(chomp: true)

EAST = Hash.new(false)
SOUTH = Hash.new(false)
WIDTH = input.first.length
HEIGHT = input.length

input.each_with_index { |row, y|
  row.each_char.each_with_index { |c, x|
    if c == ">"
      EAST[[x, y]] = true
    elsif c == "v"
      SOUTH[[x, y]] = true
    end
  }
}

EAST.freeze
SOUTH.freeze

def step(east, south)
  new_east = Hash.new(false)
  new_south = Hash.new(false)
  changed = false

  east.keys.each { |pos|
    x, y = pos
    new_pos = [(x + 1) % WIDTH, y]
    if east[new_pos] || south[new_pos]
      new_east[pos] = true
    else
      new_east[new_pos] = true
      changed = true
    end
  }

  south.keys.each { |pos|
    x, y = pos
    new_pos = [x, (y + 1) % HEIGHT]
    if new_east[new_pos] || south[new_pos]
      new_south[pos] = true
    else
      new_south[new_pos] = true
      changed = true
    end
  }

  [new_east, new_south, changed]
end

def print_cucumbers(east, south)
  (0...HEIGHT).each { |y|
    (0...WIDTH).each { |x|
      if east[[x, y]]
        print ">"
      elsif south[[x, y]]
        print "v"
      else
        print "."
      end
    }
    puts
  }
end

east = EAST
south = SOUTH
i = 0

loop do
  i += 1
  east, south, changed = step(east, south)
  break unless changed
end

puts i

